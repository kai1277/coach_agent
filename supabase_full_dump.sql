CREATE OR REPLACE FUNCTION "public"."match_casebook_cards"("query_embedding" "public"."vector", "match_count" integer) RETURNS TABLE("id" "text", "title" "text", "hypotheses" "jsonb", "probes" "jsonb", "management_tips" "jsonb", "evidence_snippets" "jsonb", "similarity" real)
    LANGUAGE "sql" STABLE
    AS $$
  select
    c.id, c.title, c.hypotheses, c.probes, c.management_tips, c.evidence_snippets,
    (1 - (c.embedding <=> query_embedding))::float4 as similarity
  from public.casebook_cards c
  order by c.embedding <=> query_embedding
  limit match_count;
$$;


ALTER FUNCTION "public"."match_casebook_cards"("query_embedding" "public"."vector", "match_count" integer) OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."match_question_templates"("query_embedding" "public"."vector", "match_count" integer) RETURNS TABLE("id" "text", "template" "text", "goal" "text", "followups" "jsonb", "similarity" real)
    LANGUAGE "sql" STABLE
    AS $$
  select
    q.id, q.template, q.goal, q.followups,
    (1 - (q.embedding <=> query_embedding))::float4 as similarity
  from public.question_templates q
  order by q.embedding <=> query_embedding
  limit match_count;
$$;


